    "1. 항공기, 선박, 철도 등 이용운송기관의 운임(보통운임기준)\n" +
    "2. 공항, 역, 부두와 호텔사이 등 송영버스요금\n" +
    "3. 숙박요금 및 식사요금\n" +
    "4. 안내자경비\n" +
    "5. 여행 중 필요한 각종세금\n" +
    "6. 국내외 공항․항만세\n" +
    "7. 관광진흥개발기금\n" +
    "8. 일정표내 관광지 입장료\n" +
    "9. 기타 개별계약에 따른 비용\n" +
    "② 제1항에도 불구하고 반드시 현지에서 지불해야 하는 경비가 있는 경우 그 내역과 금액을 여행계약서에 별도로 구분하여 표시하고, 당사는 그 사유를 안내하여야 합니다.\n" +
    "③ 여행자는 계약체결시 계약금(여행요금 중 10%이하 금액)을 당사에게 지급하여야 하며, 계약금은 여행요금 또는 손해배상액의 전부 또는 일부로 취급합니다.\n" +
    "④ 여행자는 제1항의 여행요금 중 계약금을 제외한 잔금을 여행출발 7일전까지 당사에게 지급하여야 합니다.\n" +
    "⑤ 여행자는 제1항의 여행요금을 당사자가 약정한 바에 따라 카드, 계좌이체 또는 무통장입금 등의 방법으로 지급하여야 합니다.\n" +
    "⑥ 희망여행요금에 여행자 보험료가 포함되는 경우 당사는 보험회사명, 보상내용 등을 여행자에게 설명하여야 합니다.\n" +
