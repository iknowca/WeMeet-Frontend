    "⑤ 제1항의 규정에 의하지 아니하고 여행조건이 변경되거나 제16조 내지 제18조의 규정에 의한 계약의 해제․해지로 인하여 손해배상액이 발생한 경우에는 여행출발 전 발생 분은 여행출발이전에, 여행 중 발생 분은 여행종료 후 10일 이내에 각각 정산(환급)하여야 합니다.\n" +
    "⑥ 여행자는 여행출발 후 자기의 사정으로 숙박, 식사, 관광 등 여행요금에 포함된 서비스를 제공받지 못한 경우 당사에게 그에 상응하는 요금의 환급을 청구할 수 없습니다. 다만, 여행이 중도에 종료된 경우에는 제18조에 준하여 처리합니다.\n" +
    "13. 제13조(여행자 지위의 양도)\n" +
    "① 여행자가 개인사정 등으로 여행자의 지위를 양도하기 위해서는 당사의 승낙을 받아야 합니다. 이때 당사는 여행자 또는 여행자의 지위를 양도받으려는 자가 양도로 발생하는 비용을 지급할 것을 조건으로 양도를 승낙할 수 있습니다.\n" +
    "② 전항의 양도로 발생하는 비용이 있을 경우 당사는 기한을 정하여 그 비용의 지급을 청구하여야 합니다.\n" +
    "③ 당사는 계약조건 또는 양도하기 어려운 불가피한 사정 등을 이유로 제1항의 양도를 승낙하지 않을 수 있습니다.\n" +
    "④ 제1항의 양도는 당사가 승낙한 때 효력이 발생합니다. 다만, 당사가 양도로 인해 발생한 비용의 지급을 조건으로 승낙한 경우에는 정해진 기한 내에 비용이 지급되는 즉시 효력이 발생합니다.\n" +
    "⑤ 여행자의 지위가 양도되면, 여행계약과 관련한 여행자의 모든 권리 및 의무도 그 지위를 양도 받는 자에게 승계됩니다.\n" +
    "14. 제14조(당사의 하자담보 책임)\n" +
    "① 여행자는 여행에 하자가 있는 경우에 당사에게 하자의 시정 또는 대금의 감액을 청구할 수 있습니다. 다만, 그 시정에 지나치게 많은 비용이 들거나 그 밖에 시정을 합리적으로 기대할 수 없는 경우에는 시정을 청구할 수 없습니다.\n" +
    "② 여행자는 시정 청구, 감액 청구를 갈음하여 손해배상을 청구하거나 시정 청구, 감액 청구와 함께 손해배상을 청구 할 수 있습니다.\n" +
    "③ 제1항 및 제2항의 권리는 여행기간 중에도 행사할 수 있으며, 여행종료일부터 6개월 내에 행사하여야 합니다.\n" +
    "15. 제15조(손해배상)\n" +
    "① 당사는 현지여행사 등의 고의 또는 과실로 여행자에게 손해를 가한 경우 당사는 여행자에게 손해를 배상하여야 합니다.\n" +
    "② 당사의 귀책사유로 여행자의 국외여행에 필요한 사증, 재입국 허가 또는 각종 증명서 등을 취득하지 못하여 여행자의 여행일정에 차질이 생긴 경우 당사는 여행자로부터 절차대행을 위하여 받은 금액 전부 및 그 금액의 100%상당액을 여행자에게 배상하여야 합니다.\n" +
    "③ 당사는 항공기, 기차, 선박 등 교통기관의 연발착 또는 교통체증 등으로 인하여 여행자가 입은 손해를 배상하여야 합니다. 다만, 당사가 고의 또는 과실이 없음을 입증한 때에는 그러하지 아니합니다.\n" +
    "④ 당사는 자기나 그 사용인이 여행자의 수하물 수령, 인도, 보관 등에 관하여 주의를 해태(懈怠)하지 아니하였음을 증명하지 아니하면 여행자의 수하물 멸실, 훼손 또는 연착으로 인한 손해를 배상할 책임을 면하지 못합니다.\n" +
    "16. 제16조(여행출발 전 계약해제)\n" +
    "① 당사 또는 여행자는 여행출발전 이 여행계약을 해제할 수 있습니다. 이 경우 발생하는 손해액은 ‘소비자분쟁해결기준’(공정거래위원회 고시)에 따라 배상합니다.\n" +
    "② 당사 또는 여행자는 여행출발 전에 다음 각 호의 1에 해당하는 사유가 있는 경우 상대방에게 제1항의 손해배상액을 지급하지 아니하고 이 여행계약을 해제할 수 있습니다.\n" +
    "1. 당사가 해제할 수 있는 경우\n" +
    "가. 제12조 제1항 제1호 및 제2호 사유의 경우\n" +
    "나. 여행자가 다른 여행자에게 폐를 끼치거나 여행의 원활한 실시에 현저한 지장이 있다고 인정될 때\n" +
    "다. 질병 등 여행자의 신체에 이상이 발생하여 여행에의 참가가 불가능한 경우\n" +
    "라. 여행자가 계약서에 기재된 기일까지 여행요금을 납입하지 아니한 경우\n" +
    "2. 여행자가 해제할 수 있는 경우\n" +
    "가. 제12조 제1항 제1호 및 제2호의 사유가 있는 경우\n" +
    "나. 당사가 제21조에 따른 공제 또는 보증보험에 가입하지 아니 하였거나 영업보증금을 예치하지 않은 경우\n" +
    "다. 여행자의 3촌 이내 친족이 사망한 경우\n" +
    "라. 질병 등 여행자의 신체에 이상이 발생하여 여행에의 참가가 불가능한 경우\n" +
    "마. 배우자 또는 직계존비속이 신체이상으로 3일 이상 병원(의원)에 입원하여 여행 출발 전까지 퇴원이 곤란한 경우 그 배우자 또는 보호자 1인\n" +
    "바. 당사의 귀책사유로 계약서 또는 여행일정표(여행설명서)에 기재된 여행일정대로의 여행실시가 불가능해진 경우\n" +
    "사. 제10조제1항의 규정에 의한 여행요금의 증액으로 인하여 여행 계속이 어렵다고 인정될 경우\n" +
    "17-1. 제17조(최저행사인원 미 충족시 계약해제)\n" +
    "① 당사는 최저행사인원이 충족되지 아니하여 여행계약을 해제하는 경우 여행출발 7일전까지 여행자에게 통지하여야 합니다.\n" +
    "② 당사가 여행참가자 수 미달로 전항의 기일내 통지를 하지 아니하고 계약을 해제하는 경우 이미 지급받은 계약금 환급 외에 다음 각 목의 1의 금액을 여행자에게 배상하여야 합니다.\n" +
    "가. 여행출발 1일전까지 통지시 : 여행요금의 30%\n" +
    "나. 여행출발 당일 통지시 : 여행요금의 50%\n" +
    "17-2. 제17조의 2(감염병의 발생으로 당사 또는 여행자의 계약해제)\n" +
    "① 감염병 등의 발생으로 아래 각 호의 1에 해당하는 사유가 있는 경우 ‘당사’ 또는 여행자는 제 16조 제 1항의 손해배상액을 지급하지 아니하고 계약해제를 할 수 있습니다.\n" +
    "1. 외국정부가 우리 국민에 대해 입국금지‧격리조치 등 이에 준하는 명령을 발령하여 계약을 이행할 수 없는 경우\n" +
